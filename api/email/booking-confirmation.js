const RESEND_URL = "https://api.resend.com/emails";

function escapeHtml(value) {
  return String(value || "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

module.exports = async function handler(req, res) {
  if (req.method !== "POST") {
    res.status(405).json({ error: "Use POST" });
    return;
  }

  try {
    if (!process.env.RESEND_API_KEY) {
      res.status(500).json({ error: "Missing env var: RESEND_API_KEY" });
      return;
    }

    const to = String(req.body?.to || "").trim();
    if (!to || !to.includes("@")) {
      res.status(400).json({ error: "A recipient email is required" });
      return;
    }

    const restaurant = String(req.body?.restaurant || "Madera Restaurant at Rosewood Sand Hill");
    const date = String(req.body?.date || "2026-05-27");
    const time = String(req.body?.time || "19:00");
    const covers = String(req.body?.covers || "2");
    const from = process.env.RESEND_FROM_EMAIL || "GrabGrub <onboarding@resend.dev>";
    const confirmationCode = `GG-${date.replaceAll("-", "").slice(2)}-${time.replace(":", "")}`;

    const html = `
      <div style="font-family:Inter,Arial,sans-serif;background:#fff3f6;padding:32px;color:#22131a">
        <div style="max-width:620px;margin:auto;background:#ffffff;border:2px solid #ff5a7d;padding:0;box-shadow:8px 8px 0 #2ee6a6">
          <div style="padding:22px 28px;border-bottom:2px solid #ff5a7d;background:#ff3d6e;color:#fff">
            <div style="font-size:18px;font-weight:700;letter-spacing:0.02em">GrabGrub</div>
            <div style="font-size:11px;letter-spacing:0.18em;text-transform:uppercase;color:#ffe66d;margin-top:4px">Dining concierge</div>
          </div>
          <div style="padding:30px 28px">
            <div style="display:inline-block;background:#2ee6a6;color:#06261b;border:2px solid #12b880;padding:7px 10px;font-size:12px;font-weight:800;margin-bottom:18px">Booking request confirmed</div>
            <h1 style="font-family:Georgia,serif;font-weight:400;font-size:32px;margin:0 0 10px">You're set for Madera</h1>
            <p style="line-height:1.7;color:#5b3140;margin:0">GrabGrub has prepared your dining request and sent the details below for concierge follow-up.</p>
            <div style="margin:24px 0;border-top:2px solid #ffcc00;border-bottom:2px solid #ffcc00;padding:18px 0">
              <p><strong>Confirmation code</strong><br>${escapeHtml(confirmationCode)}</p>
              <p><strong>Restaurant</strong><br>${escapeHtml(restaurant)}</p>
              <p><strong>Date</strong><br>${escapeHtml(date)}</p>
              <p><strong>Time</strong><br>${escapeHtml(time)}</p>
              <p><strong>Party size</strong><br>${escapeHtml(covers)}</p>
            </div>
            <p style="font-size:12px;line-height:1.7;color:#765062;margin:0">Final restaurant availability is subject to partner confirmation. This message was generated by GrabGrub.</p>
          </div>
        </div>
      </div>
    `;

    const response = await fetch(RESEND_URL, {
      method: "POST",
      headers: {
        "content-type": "application/json",
        authorization: `Bearer ${process.env.RESEND_API_KEY}`
      },
      body: JSON.stringify({
        from,
        to,
        subject: `GrabGrub booking request confirmed: ${restaurant}`,
        html
      })
    });

    const data = await response.json().catch(() => ({}));
    if (!response.ok) {
      res.status(response.status).json({ error: data.message || "Email request failed" });
      return;
    }

    res.status(200).json({ ok: true, id: data.id });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};
